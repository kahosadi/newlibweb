						  <li>Dewi Kunthi yang menggambarkan Kearifan</li>
						</ul>
					</p>
					<p class="text-justify">Sejak tahun 1995 Perpustakaan menggunakan nama "Perpustakaan Universitas - UKSW" untuk menunjukkan bahwa pelayanan perpustakaan dikelola secara tersentral.</p>
					<p class="text-justify">Setelah sempat beberapa tahun mengelola Pelayanan Cabang Pascasarjana dan Penelitian, dan Pelayanan Cabang Biblioterapi; namun dengan berkembangnya dan bertambahnya program studi maka Pelayanan Cabang Biblioterapi dilebur menjadi Laboratorium Konseling - Fakultas Psikologi. Sedangkan Pelayanan Cabang Pascasarjana dan Penelitian beralih fungsi sebagai Ruang Baca Pascasarjana dan dilayani di Gedung G 103 sejak Agustus 2007 sampai September 2012.</p>
