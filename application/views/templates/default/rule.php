              <h2 class="text-center">Tata Tertib Pemanfaatan Koleksi dan Fasilitas</h2>
              <br>
				          <h3 class="text-center">Pasal 1 Pengertian</h3>
                  <p class="text-justify">
					               <ul>
              						<li class="list-item">Koleksi PU adalah jenis atau bentuk produk informasi/sumber informasi dalam bentuk cetak, dan noncetak, seperti CD, VCD, koleksi elektronik/digital, yang diadakan, dikelola dan disajikan di Perpustakaan Universitas PU).</li>
              						<li class="list-item">Fasilitas PU adalah sarana dan prasarana yang disediakan dan dikelola PU untuk mendukung kelancaran pelayanan pemakai bagi pemustaka PU, seperti: fotokopi, loker, meja belajar, kursi belajar, sofa, komputer OPAC, komputer internet, komputer skripsi, komputer lab, kamar kecil/toilet, AC, selasar PU, P3K, rambu-rambu, denah.</li>
              						<li class="list-item">Pemustaka PU adalah sivitas akademika UKSW dan non sivitas akademika UKSW yang datang berkunjung ke PU dengan tujuan meminjam/menggunakan koleksi PU, belajar di PU, berbincang-bincang dengan teman, studi banding/survei atau sekadar berkunjung saja.</li>
              						<li class="list-item">Layanan Referens adalah layanan penyediaan koleksi referensi, bimbingan pendidikan literasi/penelusuran informasi, dan penyediaan meja informasi.</li>
              						<li class="list-item">Layanan Reserve adalah layanan penyediaan buku teks wajib yang jumlah eksemplarnya terbatas dan digunakan oleh banyak orang.</li>
              						<li class="list-item">Layanan Serial adalah layanan penyediaan koleksi terbitan berseri, seperti jurnal, majalah, surat kabar, tabloid, dan terbitan berkala lain.</li>
              						<li class="list-item">Layanan Koleksi Khusus adalah layanan penyediaan koleksi yang bersifat khusus, seperti skripsi, thesis, disertasi, prosiding, laporan penelitian, dan karya ilmiah pengajar yang belum terpublikasi.</li>
              						<li class="list-item">Layanan Sirkulasi adalah layanan peminjaman buku teks untuk dibawa pulang dengan jumlah dan jangka waktu yang ditetapkan.</li>
              						<li class="list-item">Layanan Multimedia adalah layanan penyediaan koleksi multimedia dan fasilitas komputer dan piranti keras lain yang diperlukan untuk mengakses semua informasi yang tersedia dalam bentuk multimedia dan elektronik.</li>
              						<li class="list-item">Sistem Layanan Terbuka adalah sistem layanan yang memungkinkan pemustaka mencari/memilih informasi/koleksi langsung di rak saji.</li>
              						<li class="list-item">Sistem Layanan Tertutup adalah sistem layanan dimana pemustaka tidak dapat langsung masuk/mengambil koleksi, melainkan dibantu oleh petugas.</li>
                        </ul>
                      </p>
				<h3 class="text-center">Pasal 2 Tata Tertib Umum</h3>
                <p class="text-justify">
					<ul>
						<li class="list-item">Bagi sivitas, setiap kali masuk ke PU wajib membawa KTM (bagi Mahasiswa)/Kartu Anggota Perpustakaan (bagi Pegawai Dosen dan Non Dosen) miliknya sendiri.</li>
						<li class="list-item">Bagi non sivitas, setiap kali masuk ke PU wajib melapor terlebih dahulu ke Petugas di Meja Informasi untuk mendapatkan Formulir Tamu/Kartu Baca/Pengesahan Kartu Anggota JASAPUSPERTI/Kartu Sakti/Kartu Super. Biaya administrasi yang harus dibayarkan adalah sebagai berikut:</li>
							<ul>
								<li class="list-item">Rp. 15.000,/hari atau Rp. 75.000,/semester untuk membaca TA (Tugas Akhir).</li>
								<li class="list-item">Rp. 5.000,/hari atau Rp. 50.000,/semester untuk membaca koleksi non TA.</li>
								<li class="list-item">Pemilik Kartu Anggota JASAPUSPERTI/Kartu Super, biaya 50% dari biaya yang berlaku.</li>
							</ul>
						<li class="list-item">Apabila akan masuk ke ruang layanan, wajib melakukan scan barcode/mengisi data di PC Pencatat Pengunjung yang ada.</li>
						<li class="list-item">Tidak diizinkan merokok di dalam ruang layanan maupun ruang kerja PU/area dalam PU.</li>
						<li class="list-item">Tidak diizinkan membawa makanan dan minuman ke ruang-ruang layanan, atau area dalam PU.</li>
						<li class="list-item">Tidak diijinkan membawa jaket/tas/map masuk ke ruang layanan.</li>
						<li class="list-item">Buku hilang:</li>
							<ul>
								<li class="list-item">Wajib mengganti dengan buku yang sama atau membayar seharga buku (harga disesuaikan dengan harga terkini) yang dihilangkan ditambah biaya administrasi sebesar Rp. 10.000,/buku, ditambah dengan denda yang ada (apabila terlambat melaporkan).</li>
								<li class="list-item">Harga ganti buku berbahasa Indonesia minimal Rp. 50.000,dan buku berbahasa asing minimal Rp. 300.000.</li>
							</ul>
						<li class="list-item">Semua proses pembayaran dilakukan di Petugas Administrasi PU.</li>
						<li class="list-item">Pemakaian ruang-ruang di PU oleh sivitas maupun non sivitas untuk kegiatan non perpustakaan wajib melapor paling lambat 1 (satu) hari sebelum tanggal pelaksanaan kegiatan kepada Pimpinan PU dengan membawa surat permohonan izin penggunaan yang ditandatangani oleh Pimpinan dan dibubuhi stempel unit/instansi terkait.</li>
						<li class="list-item">Jam layanan:</li>
							<ul>
								<li class="list-item">Senin - Jumat: 07.00-18.00 WIB.</li>
								<li class="list-item">Sabtu : 07.00-12.00 WIB.</li>
								<li class="list-item">Untuk layanan Lab Multimedia dan Lab Komputer, buka pada pukul 07.30-16.00 WIB.</li>
								<li class="list-item">Setiap hari Senin, layanan PU tutup antara pukul 09.00-10.00 WIB. (Kebaktian Senin di Balairung Universitas UKSW).</li>
								<li class="list-item">Setiap hari Jumat minggu terakhir pada bulan tersebut, layanan PU tutup antara pukul 11.45-13.00 WIB (Kebaktian Akhir Bulan PU).</li>
							</ul>
						<li class="list-item">Sistem Pelayanan di PU adalah Sistem Layanan Terbuka. Koleksi dapat dicari dan diambil sendiri, dan apabila telah selesai diletakkan di tempat khusus yang telah disediakan. Untuk menjaga urutan koleksi, tidak dianjurkan untuk mengembalikan sendiri ke rak. Pengembalian ke rak akan dilakukan oleh petugas.</li>
						<li class="list-item">Fotokopi hanya diperbolehkan untuk dilakukan di area PU. Disediakan layanan fotokopi di Lt. 2.</li>
						<li class="list-item">Tidak dibenarkan melipat halaman koleksi PU.</li>
						<li class="list-item">Tidak dibenarkan memberi tanda (dengan stabilo atau alat tulis lainnya) atau catatan/coretan pada koleksi PU.</li>
						<li class="list-item">Menjaga ketertiban, kerapian, dan kebersihan pada saat menggunakan layanan.</li>
						<li class="list-item">Melanggar tata tertib yang telah ditetapkan, dapat dikenai sanksi administratif dan/atau akademis.</li>


					</ul>
